            <h2>What is Grml?</h2>

            <p>Grml is a bootable live system (Live-CD) based on <a
              href="http://www.debian.org/">Debian</a>. Grml includes a
            collection of GNU/Linux software especially for system
            administrators and users of texttools. Grml provides automatic
            hardware detection. You can use Grml (for example) as a rescue
            system, for analyzing systems/networks or as a working environment.
            It is not necessary to install anything to a harddisk; you don't
            even need a harddisk to run it. Due to on-the-fly decompression Grml
            includes about 2.1 GB of software and documentation on the CD. You
            don't have to pay anything to use Grml - it is available for free!
